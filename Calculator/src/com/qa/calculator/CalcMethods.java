package com.qa.calculator;

public class CalcMethods {
	public void main(String[] args) {
	 
	}
	
	public static int addcalc(int cal1, int cal2 ) {
		return cal1 + cal2 ;
		
	}
	public static int minuscalc(int cal1, int cal2) {
		return cal1 - cal2;
		
	}
	
	public static int multicalc(int cal1, int cal2) {
		return cal1 * cal2 ;
		
	} 
	public static int divcalc(int cal1, int cal2) {
		return cal1 / cal2;
		
	}
	
	public static int modcalc(int cal1, int cal2) {
		return cal1 % cal2 ;
		
	}
}
