package com.qa.calculator;

public class CalcMethods {
	public static void main(String[] args) {
		
	}
	
	public static int addcalc(int cal1, int cal2, int cal3) {
		return cal1 + cal2 + cal3;
		
	}
	public static int minuscalc(int cal1, int cal2,  int cal3) {
		return cal1 - cal2 - cal3;
		
	}
	
	public static int multicalc(int cal1, int cal2,  int cal3) {
		return cal1 * cal2 * cal3;
		
	} 
	public static int divcalc(int cal1, int cal2,  int cal3) {
		return cal1 / cal2 / cal3;
		
	}
	
	public static int modcalc(int cal1, int cal2) {
		return cal1 % cal2 ;
		
	}
}
